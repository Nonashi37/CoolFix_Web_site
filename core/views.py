DAY_NAMES = {
    "ru": {1: "Понедельник", 2: "Вторник", 3: "Среда", 4: "Четверг", 5: "Пятница", 6: "Суббота", 7: "Воскресенье"},
    "ky": {1: "Дүйшөмбү",   2: "Шейшемби", 3: "Шаршемби", 4: "Бейшемби", 5: "Жума", 6: "Ишемби", 7: "Жекшемби"},
}

def _build_context(lang: str) -> dict:
    """Single source of truth for shared page context (operator card + schedule).
    Reused by home, home_ky, AND service_detail_page — that's the whole point
    of pulling it out into its own function instead of copy-pasting."""
    today_num = datetime.now().isoweekday()
    schedules = list(
        DaySchedule.objects.select_related("operator").order_by("day_of_week")
    )
    day_labels = DAY_NAMES[lang]
    for s in schedules:
        s.day_label = day_labels[s.day_of_week]

    today_schedule = next((s for s in schedules if s.day_of_week == today_num), None)

    return {
        "schedules": schedules,
        "today_schedule": today_schedule,
        "current_operator": today_schedule.operator if today_schedule else None,
        "today_num": today_num,
        "services": SERVICES[lang],
        "t": TRANSLATIONS[lang],
        "lang": lang,
        "service_url_name": "service_detail_ky" if lang == "ky" else "service_detail",
        "home_url_name": "home_ky" if lang == "ky" else "home",
    }


def home(request):
    return render(request, "core/index.html", _build_context("ru"))


def home_ky(request):
    return render(request, "core/index.html", _build_context("ky"))


def service_detail_page(request, service_slug: str, lang: str = "ru"):
    """Detail page for one service category. `lang` arrives pre-baked from
    urls.py — see the {"lang": "ru"} / {"lang": "ky"} extra kwargs there."""
    service_entry = SERVICES_DATA.get(service_slug)
    if service_entry is None:
        raise Http404(f"Service '{service_slug}' not found")

    context = _build_context(lang)
    context["service"] = service_entry[lang]
    context["service_slug"] = service_slug

    # Language toggle on a detail page should switch language and STAY on
    # this exact service — not bounce back to homepage. That's why this
    # builds a dedicated URL instead of reusing the generic set_language view.
    opposite_view = "service_detail" if lang == "ky" else "service_detail_ky"
    context["lang_switch_url"] = reverse(opposite_view, args=[service_slug])

    return render(request, "core/service_detail.html", context)


def set_language(request, lang: str):
    if lang == "ky":
        return redirect("home_ky")
    return redirect("home")